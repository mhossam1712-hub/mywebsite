function ArticleSection({ section }: { section: ArabicArticleSection }) {
  return (
    <section className={section.heading ? 'mt-10' : undefined}>
      {section.heading ? (
        <h2 className="text-right text-2xl font-bold leading-snug text-slate-950 dark:text-white">{section.heading}</h2>
      ) : null}
      {section.paragraphs?.map((paragraph) => (
        <p key={paragraph} className="mt-5 text-right text-base leading-8 text-slate-700 dark:text-gray-300">
          {paragraph}
        </p>
      ))}
      {section.items ? (
        <ul className="mt-5 list-disc space-y-3 pr-6 text-right text-slate-700 dark:text-gray-300" dir="rtl">
          {section.items.map((item) => (
            <li key={item} className="leading-7">
              {item}
            </li>
          ))}
        </ul>
      ) : null}
    </section>
  );
}

export function getArabicBlogContent(slug: string): ReactNode | null {
  const article = arabicArticles[slug];

  if (!article) {
    return null;
  }

  return (
    <div dir="rtl" lang="ar" className="text-right">
      {article.map((section, index) => (
        <ArticleSection key={`${section.heading ?? 'intro'}-${index}`} section={section} />
      ))}
    </div>
  );
}
